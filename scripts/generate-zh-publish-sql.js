/**
 * Generate a single SQL migration file that upserts the 4 Chinese blog posts
 * directly into blog_posts. User runs the file in Supabase SQL Editor — no
 * browser publisher, no Node.js publishing required.
 *
 * Usage:
 *   node scripts/generate-zh-publish-sql.js
 *   → writes migrations/20260506_blog_zh_content_publish.sql
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const POSTS = [
  { file: 'blog/content_ep1_zh.js', slug: '外国人在韩国创业-D-9-4-D-9-5签证完全指南-2026', translation_group: 'biz-immigration-ep1' },
  { file: 'blog/content_ep2_zh.js', slug: '外国人在韩国开加盟店-行业品牌指南-2026', translation_group: 'biz-immigration-ep2' },
  { file: 'blog/content_ep3_zh.js', slug: '外国人在韩国开店流程-五阶段完整指南-2026', translation_group: 'biz-immigration-ep3' },
  { file: 'blog/content_ep4_zh.js', slug: '外国企业主韩国永久居留-F-2-99到F-5指南-2026', translation_group: 'biz-immigration-ep4' },
];

function loadPost(file) {
  const src = fs.readFileSync(path.resolve(__dirname, '..', file), 'utf-8');
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(src, sandbox);
  return sandbox.window.POST;
}

// PostgreSQL dollar-quoted string. Pick a tag that can't appear in the content
// to avoid escaping. We use $zh$ since content is Chinese / English / JSON only.
function dq(s) {
  // Just in case: if "$zh$" appears in content (unlikely), pick a different tag.
  if (s.includes('$zh$')) {
    return `$zh_blog_2026$${s}$zh_blog_2026$`;
  }
  return `$zh$${s}$zh$`;
}

const lines = [
  `-- 中文 (zh) Business Immigration 4편 본문 발행 / 갱신 SQL`,
  `-- Generated by scripts/generate-zh-publish-sql.js on ${new Date().toISOString()}`,
  `--`,
  `-- 사용법: Supabase SQL Editor에 본 파일 전체 붙여넣고 Run.`,
  `-- 멱등(idempotent) — 여러 번 실행해도 안전합니다 (ON CONFLICT DO UPDATE).`,
  `-- 전제 조건: PR-A 마이그레이션이 적용되어 language, translation_group 컬럼 존재.`,
  ``,
];

const valueRows = [];
for (const entry of POSTS) {
  const post = loadPost(entry.file);
  const row = {
    slug: entry.slug,
    title: post.title,
    category: post.category,
    excerpt: post.disclaimer,
    meta_description: post.metaDescription || post.disclaimer,
    is_published: true,
    template: 'navy_v4',
    language: post.lang || 'zh',
    translation_group: entry.translation_group,
    related_services: '[]',
    content: JSON.stringify(post),
  };
  valueRows.push(`(
    ${dq(row.slug)},
    ${dq(row.title)},
    ${dq(row.category)},
    ${dq(row.excerpt)},
    ${dq(row.meta_description)},
    NULL, -- thumbnail_url
    TRUE, -- is_published
    ${dq(row.template)},
    ${dq(row.language)},
    ${dq(row.translation_group)},
    ${dq(row.related_services)}::jsonb,
    ${dq(row.content)}::jsonb,
    NOW(),
    NOW()
  )`);
}

lines.push(`INSERT INTO public.blog_posts (`);
lines.push(`  slug, title, category, excerpt, meta_description,`);
lines.push(`  thumbnail_url, is_published, template, language, translation_group,`);
lines.push(`  related_services, content, created_at, updated_at`);
lines.push(`) VALUES`);
lines.push(valueRows.join(',\n'));
lines.push(`ON CONFLICT (slug) DO UPDATE SET`);
lines.push(`  title = EXCLUDED.title,`);
lines.push(`  category = EXCLUDED.category,`);
lines.push(`  excerpt = EXCLUDED.excerpt,`);
lines.push(`  meta_description = EXCLUDED.meta_description,`);
lines.push(`  is_published = EXCLUDED.is_published,`);
lines.push(`  template = EXCLUDED.template,`);
lines.push(`  language = EXCLUDED.language,`);
lines.push(`  translation_group = EXCLUDED.translation_group,`);
lines.push(`  related_services = EXCLUDED.related_services,`);
lines.push(`  content = EXCLUDED.content,`);
lines.push(`  updated_at = NOW();`);
lines.push(``);
lines.push(`-- 검증 (별도 실행)`);
lines.push(`-- SELECT slug, language, translation_group, is_published, updated_at`);
lines.push(`-- FROM public.blog_posts WHERE language = 'zh' ORDER BY translation_group;`);
lines.push(``);

const outPath = path.resolve(__dirname, '..', 'migrations', '20260506_blog_zh_content_publish.sql');
fs.writeFileSync(outPath, lines.join('\n'), 'utf-8');
const size = fs.statSync(outPath).size;
console.log(`✓ Generated ${outPath} (${(size / 1024).toFixed(1)} KB)`);
