/**
 * Generate SQL to migrate the D-10-1 article DB row from template='toss' to
 * template='navy_v4' with the new Navy v4 content object. This stops the
 * daily cron (build-blog.js) from regenerating D-10 with the old Toss layout.
 *
 * Critical: title, slug, excerpt, meta_description, language, translation_group
 * are LEFT UNTOUCHED to preserve SEO continuity (50K+ Google impressions).
 *
 * Usage:
 *   node scripts/generate-d10-publish-sql.js
 *   → writes migrations/20260508_blog_d10_navy_v4_migration.sql
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const SLUG = 'd10-visa-korea-new-graduate-points-exemption-guide-2026';
const FILE = 'blog/content_d10_en.js';

function loadPost(file) {
  const src = fs.readFileSync(path.resolve(__dirname, '..', file), 'utf-8');
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(src, sandbox);
  return sandbox.window.POST;
}

function dq(s) {
  if (s.includes('$d10$')) return `$d10_blog_2026$${s}$d10_blog_2026$`;
  return `$d10$${s}$d10$`;
}

const post = loadPost(FILE);

const lines = [
  `-- D-10-1 article — design migration to Navy v4 (template change + content update)`,
  `-- Generated by scripts/generate-d10-publish-sql.js on ${new Date().toISOString()}`,
  `--`,
  `-- 사용법: Supabase SQL Editor에 본 파일 전체 붙여넣고 Run.`,
  `-- 멱등(idempotent) — 여러 번 실행해도 안전합니다.`,
  `--`,
  `-- 보존되는 항목 (SEO 5만+ 노출 보호 — 절대 변경 안 함):`,
  `--   - slug, title, excerpt, meta_description`,
  `--   - language, translation_group, is_published`,
  `--   - created_at`,
  `--`,
  `-- 변경되는 항목:`,
  `--   - template: 'toss' → 'navy_v4'  (이게 핵심: cron이 더 이상 D-10을 재생성하지 않음)`,
  `--   - content: 새 POST 객체 (Navy v4 schema)`,
  `--   - updated_at: now()`,
  ``,
  `UPDATE public.blog_posts`,
  `SET`,
  `  template = 'navy_v4',`,
  `  content = ${dq(JSON.stringify(post))}::jsonb,`,
  `  updated_at = NOW()`,
  `WHERE slug = ${dq(SLUG)};`,
  ``,
  `-- 검증 (별도 실행)`,
  `-- SELECT slug, template, language, translation_group, is_published,`,
  `--        title, LEFT(meta_description, 80) AS meta_preview, updated_at`,
  `-- FROM public.blog_posts`,
  `-- WHERE slug = '${SLUG}';`,
  ``,
];

const outPath = path.resolve(__dirname, '..', 'migrations', '20260508_blog_d10_navy_v4_migration.sql');
fs.writeFileSync(outPath, lines.join('\n'), 'utf-8');
const size = fs.statSync(outPath).size;
console.log(`✓ Generated ${outPath} (${(size / 1024).toFixed(1)} KB)`);
