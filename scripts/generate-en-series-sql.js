/**
 * Generate a single SQL file that:
 *   1) Applies the template column migration (idempotent)
 *   2) Upserts the 4 English business-immigration posts with
 *      template='navy_v4' and is_published=true
 *   3) Re-asserts is_published=true + template='toss' on d10 / f2r
 *      (safety — recovers the original posts if they got hidden)
 *
 * Output: scripts/publish-en-series.sql
 *
 * Run: node scripts/generate-en-series-sql.js
 * Then paste scripts/publish-en-series.sql into Supabase SQL Editor.
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');

const POSTS = [
  { file: 'blog/content_ep1_en.js', slug: 'start-a-business-in-korea-as-a-foreigner-2026' },
  { file: 'blog/content_ep2_en.js', slug: 'korea-business-visa-investment-and-family-guide-2026' },
  { file: 'blog/content_ep3_en.js', slug: 'how-to-open-a-store-in-korea-as-a-foreigner-2026' },
  { file: 'blog/content_ep4_en.js', slug: 'how-to-stay-in-korea-long-term-as-a-business-owner-2026' },
];

function loadPost(file) {
  const src = fs.readFileSync(path.resolve(__dirname, '..', file), 'utf-8');
  const sandbox = { window: {} };
  vm.createContext(sandbox);
  vm.runInContext(src, sandbox);
  return sandbox.window.POST;
}

// Pick a dollar-quote tag that doesn't appear in the content.
function pickTag(content) {
  for (const tag of ['', 'B1', 'B2', 'B3', 'B4', 'B5']) {
    const delim = `$${tag}$`;
    if (!content.includes(delim)) return tag;
  }
  throw new Error('Could not find a safe dollar-quote tag');
}

function dq(s) {
  const tag = pickTag(s);
  return `$${tag}$${s}$${tag}$`;
}

const parts = [];
parts.push(`-- Publish EN Business Immigration series (EP01 ~ EP04)
-- Generated by scripts/generate-en-series-sql.js
-- Safe to re-run: ON CONFLICT (slug) upserts; d10/f2r statements are idempotent.
-- Paste this entire file into Supabase → SQL Editor → Run.

-- 1. Ensure the 'template' column exists (idempotent).
ALTER TABLE public.blog_posts
  ADD COLUMN IF NOT EXISTS template TEXT NOT NULL DEFAULT 'toss';

-- 2. Ensure the 'slug' column is UNIQUE so ON CONFLICT below works.
--    (idempotent — skips if already present)
DO $migrate$
BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM pg_constraint
    WHERE conrelid = 'public.blog_posts'::regclass
      AND conname  = 'blog_posts_slug_unique'
  ) THEN
    -- Remove any accidental duplicates first, keeping the most recent row.
    DELETE FROM public.blog_posts a USING public.blog_posts b
    WHERE a.slug = b.slug AND a.created_at < b.created_at;
    ALTER TABLE public.blog_posts
      ADD CONSTRAINT blog_posts_slug_unique UNIQUE (slug);
  END IF;
END
$migrate$;

-- 3. Ensure the existing d10 / f2r posts stay published+toss
--    (recovers them if anything earlier flipped the flag).
UPDATE public.blog_posts
SET is_published = TRUE, template = 'toss'
WHERE slug = 'd10-visa-korea-new-graduate-points-exemption-guide-2026';

UPDATE public.blog_posts
SET is_published = TRUE, template = 'toss'
WHERE slug = 'f2r-visa-korea-regional-university-graduate-residence-guide-2026';
`);

for (const entry of POSTS) {
  const post = loadPost(entry.file);
  const content = JSON.stringify(post);
  parts.push(`
-- ─────────────────────────────────────────────────────────────────
-- EP${post.episodeNo} · ${post.title.slice(0, 70).replace(/\n/g, ' ')}
-- ─────────────────────────────────────────────────────────────────
INSERT INTO public.blog_posts (
  title, slug, category, excerpt, meta_description, thumbnail_url,
  is_published, template, related_services, content, created_at, updated_at
) VALUES (
  ${dq(post.title)},
  '${entry.slug}',
  '${post.category}',
  ${dq(post.disclaimer || '')},
  ${dq(post.disclaimer || '')},
  NULL,
  TRUE,
  'navy_v4',
  '[]',
  ${dq(content)},
  NOW(),
  NOW()
)
ON CONFLICT (slug) DO UPDATE SET
  title            = EXCLUDED.title,
  category         = EXCLUDED.category,
  excerpt          = EXCLUDED.excerpt,
  meta_description = EXCLUDED.meta_description,
  is_published     = EXCLUDED.is_published,
  template         = EXCLUDED.template,
  related_services = EXCLUDED.related_services,
  content          = EXCLUDED.content,
  updated_at       = NOW();
`);
}

parts.push(`
-- 4. Verify the state afterwards.
SELECT slug, is_published, template, LEFT(title, 60) AS title
FROM public.blog_posts
ORDER BY created_at DESC;
`);

const out = path.resolve(__dirname, 'publish-en-series.sql');
fs.writeFileSync(out, parts.join('\n'), 'utf-8');
console.log(`✓ Wrote ${out} (${(fs.statSync(out).size / 1024).toFixed(1)} KB)`);
